
#Faça um algoritmo que calcule a media aritmetica de dois numeros e imprima o resultado 

numero = int(input("Escreva um numero inteiro para ver a media aritmetica: "))
numero2 = int(input("Escreva outro numero inteiro para ver a media aritmetica: "))

resultado = (numero + numero2) / 2 

print(f"A media aritmetica dos numeros {numero} e {numero2} é: {resultado}")

