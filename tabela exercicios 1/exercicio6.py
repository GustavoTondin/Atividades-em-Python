
#Faça um algoritmo que calcule a potencia de um numero e imprima o resultado

numero = int(input("Escreva um numero inteiro para ver sua potencia: "))
numero2 = int(input(f"Escreva a qual potencia o numero {numero} sera elevado: "))

numero3 = numero ** numero2

print(f"A potencia do numero {numero} elevado a {numero2} é: {numero3}")

