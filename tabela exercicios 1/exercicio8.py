
#Faça um algoritmo que calcule a area de um retangulo de base e altura informados pelo usuário e imprima o resultado

base = float(input("Escreva o tamanho da base do retangulo: "))
altura = float(input("Escreva a altura do retangulo: "))

area = base * altura  

print(f"A área de um retangulo de base {base} e altura {altura} é: {area}")

