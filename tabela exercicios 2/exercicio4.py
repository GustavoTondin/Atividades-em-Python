#Uma pessoa pesa 65kg e mede 1,70m. Qual é o seu índice de massa corporal (IMC)?
#Considere a fórmula IMC = peso / altura².

peso = 65
altura = 1.7

imc = peso / (altura * altura)

print(f"O IMC de uma pessoa que pesa {peso} e tem um altura de {altura} é: {imc:.2F}")

            
       
            
            











